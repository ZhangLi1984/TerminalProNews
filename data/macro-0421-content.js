  <p class="text-red-900 text-sm">市场解读Q1 GDP 5.0%为强劲复苏信号，但拆解发现：增长主要靠出口（14.7%）拉动，消费仅2.4%，投资靠基建支撑。3月单月数据全面走弱（出口2.5%、社零1.7%、固投当月1.6%），春节错位解释可能过于简单。<strong>若Q2出口因中东冲突和全球需求走弱而失速，GDP可能快速回落至4.5%以下</strong>。</p>
